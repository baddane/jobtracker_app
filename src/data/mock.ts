// Function to initialize mock data in localStorage
export function initializeMockData() {
  if (typeof window !== "undefined") {
    const existingData = localStorage.getItem("job-apply-track-applications");
    if (!existingData) {
      localStorage.setItem(
        "job-apply-track-applications",
        JSON.stringify({
          state: {
            applications: mockApplications,
            searchQuery: "",
            filters: {},
            sort: { field: "applicationDate", order: "desc" },
          },
          version: 0,
        })
      );
    }
  }
}
